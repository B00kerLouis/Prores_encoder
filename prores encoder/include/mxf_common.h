// ============================================================
//  9. Partition Pack builder
// ============================================================
static inline std::vector<uint8_t> build_partition(
    const UL16& key, uint64_t thisPart, uint64_t prevPart, uint64_t footerPart,
    uint64_t headerBC, uint64_t indexBC, uint32_t indexSID,
    uint64_t bodyOff, uint32_t bodySID,
    const UL16& opPat, const std::vector<UL16>& ecs,
    uint32_t kagSize = 0)
{
    std::vector<uint8_t> v;
    w16(v,1); w16(v,3);
    w32(v,kagSize);
    w64(v,thisPart); w64(v,prevPart); w64(v,footerPart);
    w64(v,headerBC); w64(v,indexBC);
    w32(v,indexSID); w64(v,bodyOff); w32(v,bodySID);
    wbytes(v,opPat.data(),16);
    w32(v,(uint32_t)ecs.size()); w32(v,16);
    for(auto& ec:ecs) wbytes(v, ec.data(), 16);
    return klv_vec(key, v);
}

// ============================================================
//  10. RIP builder
// ============================================================
struct RIPEntry { uint32_t bodySID; uint64_t off; };

static inline std::vector<uint8_t> build_rip(const std::vector<RIPEntry>& entries) {
    std::vector<uint8_t> body;
    for(auto& e:entries) { w32(body,e.bodySID); w64(body,e.off); }
    std::vector<uint8_t> bt; ber(bt, body.size()+4);
    uint32_t total = 16 + (uint32_t)bt.size() + (uint32_t)body.size() + 4;
    std::vector<uint8_t> out;
    wbytes(out, K_RIP.data(), 16); ber(out, body.size()+4); wbytes(out, body);
    w32(out, total);
    return out;
}

// ============================================================
//  11. Index Table Segment builder (VBR)
// ============================================================
static inline std::vector<uint8_t> build_index_segment(
    int fpsNum, int fpsDen, int64_t startPos,
    const std::vector<uint64_t>& offsets,
    const std::vector<uint32_t>& audioSlices,
    int indexSID, int bodySID,
    bool hasAudio, bool hasSysItem)
{
    std::vector<uint8_t> b;
    lt_uuid(b, 0x3c0a, make_uuid());
    {std::vector<uint8_t> v; w32(v,(uint32_t)fpsNum); w32(v,(uint32_t)fpsDen); lt_item(b,0x3f0b,v);}
    {std::vector<uint8_t> v; w64(v,(uint64_t)startPos); lt_item(b,0x3f0c,v);}
    {std::vector<uint8_t> v; w64(v,(uint64_t)offsets.size()); lt_item(b,0x3f0d,v);}
    {std::vector<uint8_t> v; w32(v,0); lt_item(b,0x3f05,v);}
    {std::vector<uint8_t> v; w32(v,(uint32_t)indexSID); lt_item(b,0x3f06,v);}
    {std::vector<uint8_t> v; w32(v,(uint32_t)bodySID); lt_item(b,0x3f07,v);}
    uint8_t sliceCnt = hasAudio ? 1 : 0;
    lt_u8(b, 0x3f08, sliceCnt);
    {
        int nDelta = hasSysItem ? (hasAudio?3:2) : (hasAudio?2:1);
        std::vector<uint8_t> v; w32(v,(uint32_t)nDelta); w32(v,6);
        if(hasSysItem) { v.push_back(0xFF); v.push_back(0); w32(v,0);
                         v.push_back(0); v.push_back(0); w32(v,SYS_ITEM_SIZE); }
        else           { v.push_back(0); v.push_back(0); w32(v,0); }
        if(hasAudio)   { v.push_back(0xFF); v.push_back(1); w32(v,0); }
        lt_item(b, 0x3f09, v);
    }
    {
        uint32_t esz = 11 + 4*(uint32_t)sliceCnt;
        std::vector<uint8_t> v; w32(v,(uint32_t)offsets.size()); w32(v,esz);
        for(size_t i=0;i<offsets.size();i++) {
            v.push_back(0); v.push_back(0); v.push_back(0x80);
            w64(v, offsets[i]);
            if(hasAudio) w32(v, audioSlices[i]);
        }
        lt_item(b, 0x3f0a, v);
    }
    return klv_vec(K_INDEX_TABLE, b);
}

// ============================================================
//  11b. CBR Index Table Segment for OP-Atom audio
// ============================================================
static inline std::vector<uint8_t> build_audio_cbr_index(
    int srNum, int srDen, int editUnitByteCount, int indexSID, int bodySID)
{
    std::vector<uint8_t> b;
    lt_uuid(b, 0x3c0a, make_uuid());
    {std::vector<uint8_t> v; w32(v,(uint32_t)srNum); w32(v,(uint32_t)srDen); lt_item(b,0x3f0b,v);}
    {std::vector<uint8_t> v; w64(v,0); lt_item(b,0x3f0c,v);}
    {std::vector<uint8_t> v; w64(v,0); lt_item(b,0x3f0d,v);}
    {std::vector<uint8_t> v; w32(v,(uint32_t)editUnitByteCount); lt_item(b,0x3f05,v);}
    {std::vector<uint8_t> v; w32(v,(uint32_t)indexSID); lt_item(b,0x3f06,v);}
    {std::vector<uint8_t> v; w32(v,(uint32_t)bodySID); lt_item(b,0x3f07,v);}
    lt_u8(b, 0x3f08, 0);
    {
        std::vector<uint8_t> v;
        // OP-Atom audio CBR index layout:
        // one zeroed system-item placeholder entry plus one audio-stream entry.
        w32(v, 2); w32(v, 6);
        v.push_back(0); v.push_back(0); w32(v, 0);
        v.push_back(0); v.push_back(0); w32(v, 0);
        lt_item(b, 0x3f09, v);
    }
    return klv_vec(K_INDEX_TABLE, b);
}

