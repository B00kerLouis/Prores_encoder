static inline std::vector<uint8_t> mk_cdci(const UUID16& uid, uint32_t linkedTrk,
    const Config& cfg, int aspW, int aspH, int64_t dur) {
    std::vector<uint8_t> b;
    uint8_t vb = static_cast<uint8_t>(cfg.variant);
    lt_uuid(b,0x3c0a,uid); lt_u32(b,0x3006,linkedTrk);
    lt_rational(b,0x3001,cfg.fpsNum,cfg.fpsDen);
    lt_u64(b,0x3002,(uint64_t)dur); lt_ul(b,0x3004,EC_RDD36);
    lt_ul(b,0x3201,prores_codec_ul(vb));
    lt_u32(b,0x3202,(uint32_t)cfg.height); lt_u32(b,0x3203,(uint32_t)cfg.width);
    lt_u32(b,0x3204,(uint32_t)cfg.height); lt_u32(b,0x3205,(uint32_t)cfg.width);
    lt_u32(b,0x3206,0); lt_u32(b,0x3207,0);
    lt_u32(b,0x3208,(uint32_t)cfg.height); lt_u32(b,0x3209,(uint32_t)cfg.width);
    lt_u32(b,0x320a,0); lt_u32(b,0x320b,0);
    lt_rational(b,0x320e,aspW,aspH); lt_u8(b,0x320c,0);
    {
        std::vector<uint8_t> v;
        uint32_t l1=42,l2=0;
        if(cfg.width==1280&&cfg.height==720) l1=26;
        else if(cfg.width==720&&cfg.height==480){l1=20;l2=283;}
        else if(cfg.width==720&&cfg.height==576){l1=23;l2=336;}
        else if(cfg.height>=2000) l1=1;
        w32(v,2); w32(v,4); w32(v,l1); w32(v,l2);
        lt_item(b,0x320d,v);
    }
    {
        uint8_t ss=4;
        if(cfg.width==1280&&cfg.height==720) ss=7;
        else if(cfg.width==720&&cfg.height==480) ss=3;
        else if(cfg.width==720&&cfg.height==576) ss=1;
        else if(cfg.height>=2000) ss=0;
        lt_u8(b,0x3215,ss);
    }
    lt_u32(b,0x3301,10);
    uint32_t hSub = (vb>=5)?1:2;
    lt_u32(b,0x3302,hSub); lt_u32(b,0x3308,1); lt_u8(b,0x3303,0);
    lt_u32(b,0x3304,64); lt_u32(b,0x3305,940); lt_u32(b,0x3306,897);
    if(cfg.color.valid) {
        static const UL16 z{};
        if(cfg.color.colorPrimaries!=z)  lt_ul(b,0x3219,cfg.color.colorPrimaries);
        if(cfg.color.transferFunction!=z) lt_ul(b,0x3210,cfg.color.transferFunction);
        if(cfg.color.codingEquations!=z) lt_ul(b,0x321a,cfg.color.codingEquations);
    }
    return klv_vec(SK_CDCI, b);
}

