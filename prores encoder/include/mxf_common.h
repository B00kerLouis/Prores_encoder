// Timestamp
static inline std::vector<uint8_t> now_ts() {
    time_t t = time(nullptr); tm* u = gmtime(&t);
    std::vector<uint8_t> b;
    w16(b,(uint16_t)(1900+u->tm_year)); w8(b,(uint8_t)(u->tm_mon+1));
    w8(b,(uint8_t)u->tm_mday); w8(b,(uint8_t)u->tm_hour);
    w8(b,(uint8_t)u->tm_min); w8(b,(uint8_t)u->tm_sec); w8(b,0);
    return b;
}

// ============================================================
//  6. GC System Item builder (SMPTE 326M) — OP-1a only
// ============================================================
static inline uint8_t gc_cp_rate(int fpsNum, int fpsDen) {
    int fps = (fpsDen==1) ? fpsNum : fpsNum/1000;
    const int rates[] = {24,25,30,48,50,60,72,75,90,96,100,120};
    for(int i=0;i<12;i++) if(rates[i]==fps)
        return (uint8_t)(((i+1)<<1)|(fpsDen!=1?1:0));
    return 0;
}

static inline void encode_smpte_tc(uint8_t* tc16, int64_t pos, int fpsNum, int fpsDen, bool drop) {
    int fps = (fpsDen==1) ? fpsNum : (fpsNum+fpsDen/2)/fpsDen;
    if(fps<1) fps=1;
    int ff=(int)(pos%fps); int64_t s=pos/fps;
    int ss=(int)(s%60); int64_t m=s/60; int mm=(int)(m%60); int hh=(int)(m/60)%24;
    tc16[0]=(uint8_t)(((ff/10)<<4)|(ff%10));
    tc16[1]=(uint8_t)(((ss/10)<<4)|(ss%10));
    tc16[2]=(uint8_t)(((mm/10)<<4)|(mm%10));
    tc16[3]=(uint8_t)(((hh/10)<<4)|(hh%10));
    if(drop) tc16[0]|=0x40;
    memset(tc16+4,0,12);
}

static inline std::vector<uint8_t> build_system_item(int64_t frame, int fpsNum, int fpsDen,
    int64_t tcStart, bool drop, bool hasPic, bool hasSnd)
{
    uint8_t bitmap = 0x50;
    if(hasPic) bitmap|=0x08;
    if(hasSnd) bitmap|=0x04;

    std::vector<uint8_t> val; val.reserve(57);
    w8(val,bitmap); w8(val,gc_cp_rate(fpsNum,fpsDen)); w8(val,0);
    w16(val,0); w16(val,(uint16_t)(frame&0xffff));
    wbytes(val, EC_GC.data(), 16);
    for(int i=0;i<17;i++) w8(val,0);
    w8(val,0x81);
    uint8_t tc16[16];
    encode_smpte_tc(tc16, tcStart+frame, fpsNum, fpsDen, drop);
    wbytes(val, tc16, 16);

    std::vector<uint8_t> out; out.reserve(SYS_ITEM_SIZE);
    wbytes(out, K_SYSTEM_ITEM.data(), 16); ber(out,val.size()); wbytes(out,val);
    wbytes(out, K_EMPTY_PKG.data(), 16);   ber(out,0);
    return out;
}

// ============================================================
//  7. Timecode parser
// ============================================================
static inline int64_t parse_tc(const std::string& tc, int fps) {
    int hh=0,mm=0,ss=0,ff=0;
    if(tc.size()>=11) {
        hh=std::stoi(tc.substr(0,2)); mm=std::stoi(tc.substr(3,2));
        ss=std::stoi(tc.substr(6,2)); ff=std::stoi(tc.substr(9,2));
    }
    return (int64_t)hh*3600*fps + (int64_t)mm*60*fps + (int64_t)ss*fps + ff;
}

